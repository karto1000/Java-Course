import java.util.Scanner;

  class exerc_2{
    
/*This tutorial we do a simple addition usin variables pre setted with a value*/
    
    public static void main (String args[]){
      
//--------------Varibles declarations--------------------
      
      /* working with a simple math operator*/
      
      int girls, boys, people;
      
      girls = 11;
      boys = 3;
      people = girls + boys;
      
      System.out.println(people);

//--------------Variables declaration---------------------
     
      /*Working with division using int. If we have a decimal number as result 
      of our division and we declarated as int the variable it will show us just
      de integre part as result*/

      
      int girls2, boys2, people2;
      
      girls2 = 11;
      boys2 = 3;
      people2 = girls2 / boys2;
      
      System.out.println(people2);
      
//---------------Variables declarations----------------------
      
      /* with a float variable declareted all results of decimal numbers will be printed*/
      
      float girls3, boys3, people3;
      
      girls3 = 11;
      boys3 = 3;
      people3 = girls3 / boys3;
      
      System.out.println(people3);
      
//---------------Variables declarations----------------------
      
      /* with a modules operator (%) we show the left value of a division*/
      
      int girls4, boys4, people4;
      
      girls4 = 11;
      boys4 = 3;
      people4 = girls4 % boys4;
      
      System.out.println(people4);
      
    }//fim do metodo main
    
  }//fim do programa

